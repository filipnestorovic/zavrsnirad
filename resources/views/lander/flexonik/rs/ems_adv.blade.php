        <p>
            Pre samo 16 meseci imala sam 94,7kg. Zbog prirode posla, i gustog rasporeda, jela sam mnogo brze i nezdrave hrane. Vremenom se situacija otrgla kontroli i nisam više mogla da se pogledam u ogledalu. Nikako mi se nije svidjalo to što vidim - jednu debelu i ružnu ženu. Nije mi dugo trebalo da zamrzim sopstveno telo.
        </p>
        <p>
            Toliko sam se loše osećala da kada me je Bogdan zaprosio, da sam prvo odbila! Svaka devojka sanja da bude lepa i vitka na svom venčanju, nisam mogla ni da zamislim da ceo život gledam slike gde sam kao kit u venčanici. Ta pomisao me je užasavala. Na kraju mi je ta Bogdanova prosidba promenila život (Naravno da sam pristala :) ) Odlučila sam da do svog venčanja moram da smršam, kako znam i umem.
        </p>
        <p>
            <b>Nedostatak vremena je i dalje bio veliki problem</b>. Hirurg sam, usled trenutne situacije preopterećeni smo poslom i ne mogu jednostavno da uzmem odmor, ljudski životi su u mojim rukama.
        </p>
        <p>
            Dijete, teretane i slični tretmani mi takođe ne odgovaraju. Za treninge nemam vremena, a ostale stvari jednostavno koštaju puno.
        </p>
        <p>
            Morala sam da nađem drugu opciju.
        </p>
        <p>
            U slobodno vreme guglala sam razne termine, “brzo i lako mršavljenje” i “kako da brzo povečam mišićnu masu” znajući da mišiči tope masti, pa sam se nadala da ću uz više mišića brže mršaviti.
        </p>
        <p>
            Posle par dana počele su da mi iskaču raznorazne reklame na tu temu. Privatni treninzi, nutricionisti, sprejevi, kapi, svaka moguća stvar vezana za mršavljenje.
        </p>
        <p>
            Jedna reklama mi je privukla pažnju, i odvela me je na stranicu gde se pominje neki uređaj, čiji proizvođači obećavaju da će da naprave <b>“atletu od svakog tela”</b>. Malo sam istraživala na tu temu, konsultovala se sa kolegama, i videla baš dosta preporuka i pozitivnih utisaka. Iako nisam bila baš sigurna u to odlučila sam da dam šansu tom proizvodu. Svakako nisam mogla ništa da izgubim!
        </p>
        <img align="left" class="right-img" src="{{ asset('/') }}flexonikFiles/ems/adv/prod.jpg" style="padding-right: 15px;"/>
