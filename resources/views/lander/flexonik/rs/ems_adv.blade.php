        </p>
        <p>
            Istog trenutka sam ga otpakovala i počela da koristim. Osećaj je u početku bio čudan, ali prijatan kao neka sportska masaža. Osećala sam pulsiranje, i kao neke trnce da prolaze kroz telo. Nije bilo ništa strašno, svakako sam se posle drugog tretmana navikla, i više nisam osećala tako nešto.
        </p>
        <p>
            Koristila sam trenažer <b>3 puta dnevno po 25 minuta</b>. Bukvalno stavim na stomak, i dok sedim tretman je završen. Koža je ubrzo postala elastičnija, a primetila sam da se masti i celulit povlače. Posle prve nedelje, rezultat više nego fin - <b>3kg manje</b>.
        </p>
        <p>
            Posle <b>60ak dana</b>, nisam mogla samu sebe da prepoznam. <b>26 kilograma manje</b>, vratila sam se na kilažu koju sam imala pre trudnoće. Okruženje nije moglo da veruje kakvu sam promenu napravila u svom izgledu.
        </p>
        <p>
            Osećala sam se mlađe i puna samopouzdanja. Već sam bila i više nego spremna za venčanje, ali nisam htela da prestanem da koristim ovaj uređaj. Htela sam da napravim još bolje rezultate.
        </p>
        <img align="left" class="right-img" src="{{ asset('/') }}flexonikFiles/ems/adv/prod2.jpg" style="padding-right: 15px;"/>
