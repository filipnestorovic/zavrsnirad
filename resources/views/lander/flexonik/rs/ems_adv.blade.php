            Kako sam topila masti, tako sam i povećavala mišićnu masu, i sve to koristeći isti uređaj. Ono što mi se mnogo sviđa kod ovog uređaja je što može da se koristi na bilo kom delu tela, a ne samo na stomaku.
        </p>
        <p>
            Mogu da kažem da je ovaj EMS trenažer više nego opravdao očekivanja, koja iskreno u početku nisu bila velika. Posle tako kratkog korišćenja, <b>masti su nestale a mišići zategnuti</b>. Vitko, zategnuto telo, malo je reći da sam bila oduševljena. Nisam mogla da verujem da sam to ja.
        </p>
        <p>
            Prosto je neverovatno koliko je tehnologija napredovala. Jedan uređaj koji sam kupila preko interneta mi je pomogao da smršam i da dovedem telo u željeno stanje bez ikakvog napora ili dijete.
        </p>
